export class BlackSaddles {
    name: string;
    dressage: string;
    gallop: string;
    jumping: string;
    speed: string;
    stamina: string;
    trot: string;
    id: string;

constructor(name: string, dressage: string, gallop: string, jumping: string, speed: string, stamina: string, trot: string, id: string) {
    this.name = name;
    this.dressage = dressage;
    this.gallop = gallop;
    this.jumping = jumping;
    this.speed = speed;
    this.stamina = stamina;
    this.trot = trot;
    this.id = id
}
}
